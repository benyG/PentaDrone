    // Load form values
    protected function loadFormValues()
    {
        // Load from form
        global $CurrentForm;

        // Check field name 'code' first before field var 'x_code'
        $val = $CurrentForm->hasValue("code") ? $CurrentForm->getValue("code") : $CurrentForm->getValue("x_code");
        if (!$this->code->IsDetailKey) {
            if (IsApi() && $val === null) {
                $this->code->Visible = false; // Disable update for API request
            } else {
                $this->code->setFormValue($val);
            }
        }
        if ($CurrentForm->hasValue("o_code")) {
            $this->code->setOldValue($CurrentForm->getValue("o_code"));
        }

        // Check field name 'control_familyName' first before field var 'x_control_familyName'
        $val = $CurrentForm->hasValue("control_familyName") ? $CurrentForm->getValue("control_familyName") : $CurrentForm->getValue("x_control_familyName");
        if (!$this->control_familyName->IsDetailKey) {
            if (IsApi() && $val === null) {
                $this->control_familyName->Visible = false; // Disable update for API request
            } else {
                $this->control_familyName->setFormValue($val);
            }
        }

        // Check field name 'description' first before field var 'x_description'
        $val = $CurrentForm->hasValue("description") ? $CurrentForm->getValue("description") : $CurrentForm->getValue("x_description");
        if (!$this->description->IsDetailKey) {
            if (IsApi() && $val === null) {
                $this->description->Visible = false; // Disable update for API request
            } else {
                $this->description->setFormValue($val);
            }
        }
    }

