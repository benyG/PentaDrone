    // Return basic search SQL
    protected function basicSearchSql($arKeywords, $type)
    {
        $where = "";
        $this->buildBasicSearchSql($where, $this->Nidentifier, $arKeywords, $type);
        $this->buildBasicSearchSql($where, $this->N_ordre, $arKeywords, $type);
